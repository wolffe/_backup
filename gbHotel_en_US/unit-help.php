<h2 class="hp-inner-title">Help</h2>
<p>Quick guide on how to use HotelPress Booking Manager.</p>

<h2>How to add a new booking</h2>
<p>In order to add a new booking, go to <b>NEW BOOKING</b> page, select a room and add tourist details. Using the calendar controls, you can check for already booked dates or for existing vacancies. Booked dates appear in red.</p>
<p>The &quot;Extra Booking Package Details&quot; should contain more details, such as included breakfast, or pool access, or more tourist details.</p>

<h2>How to view current bookings (for a selected day)</h2>
<p>Go to <b>MANAGE BOOKINGS</b> and select a day from the date popup area. You will get a diagram of all rooms with booked ones marked with red.</p>

<h2>How to add, remove or retire a room</h2>
<p>The <b>MANAGE ROOMS</b> page helps you add new rooms, delete existing ones or retire rooms - mark as cleaning, repairing or simply unavailable. The retire action is designed as a brush icon.</p>
<p>A room can be simple, double or other specified types. If you want to add a new type, leave the existing types box empty and fill in the new room type. It will automatically be added to the list next time.</p>

<h2>Bookings per room</h2>
<p>The <b>BOOKINGS/ROOM</b> page is the opposite of the bookings-by-date page and displays all bookings for a selected room, past, present and future.</p>
<p>This page also allows you to cancel (remove) current bookings.</p>

